package com.example.e_market;

public class Item {
    private String usr_email;
    private String sup_email;
    private String item_name;
    private String item_price;
    private String stock_qyt;

    public String getUsr_email() {
        return usr_email;
    }

    public void setUsr_email(String usr_email) {
        this.usr_email = usr_email;
    }

    public String getSup_email() {
        return sup_email;
    }

    public void setSup_email(String sup_email) {
        this.sup_email = sup_email;
    }

    public String getItem_name() {
        return item_name;
    }

    public void setItem_name(String item_name) {
        this.item_name = item_name;
    }

    public String getItem_price() {
        return item_price;
    }

    public void setItem_price(String item_price) {
        this.item_price = item_price;
    }

    public String getStock_qyt() {
        return stock_qyt;
    }

    public void setStock_qyt(String stock_qyt) {
        this.stock_qyt = stock_qyt;
    }
}
